function Fig4RVPS_SDPpaper2022(LionHData,LionHModel,LionHModelMin,LionHModelMax,LionHDataMin,LionHDataMax,...
    HyenaHData,HyenaHModel,HyenaHModelMin,HyenaHModelMax,HyenaHDataMin,HyenaHDataMax,...
    LionScavData,LionScavModel,LionScavModelMin,LionScavModelMax,LionScavDataMin,LionScavDataMax,...
    HyenaScavData,HyenaScavModel,HyenaScavModelMin,HyenaScavModelMax,HyenaScavDataMin,HyenaScavDataMax)

% Create figure
figure1 = figure('PaperUnits','centimeters','PaperType','<custom>','PaperSize',[30.5 24.7],'Color',[1 1 1]);
% Create axes
axes1 = axes('Position',[0.206473214285714 0.17923186344239 0.698526785714283 0.787019914651495]);
hold(axes1,'on');

%get best fit line
%first put all data together; because we want fit for the entire data put
%togetehr
DataFrac = [LionHData; LionScavData; HyenaHData; HyenaScavData];
ModelFrac = [LionHModel; LionScavModel; HyenaHModel; HyenaScavModel];
[fitobject, gof] = fit(DataFrac,ModelFrac,'poly1');

%plot (use smaller x axis increments for bettere visulaising)
BestFitLine1 = fitobject.p1*(0:0.01:1.5) + fitobject.p2;
plot(0:0.01:1.5,BestFitLine1,'LineWidth',0.75,'LineStyle','--','Color',[0.501960813999176 0.501960813999176 0.501960813999176]);

%plotting the rest: Lion hunting error bar
errorbar(LionHData,LionHModel,abs(LionHModel-LionHModelMin),abs(LionHModel-LionHModelMax),...
    abs(LionHData-LionHDataMin),abs(LionHData-LionHDataMax),'DisplayName','Li, h',...
    'MarkerSize',15,'MarkerFaceColor',[0.466666668653488 0.674509823322296 0.18823529779911],...
    'Marker','o','LineStyle','none','LineWidth',1,'Color',[0.466666668653488 0.674509823322296 0.18823529779911]);
%plotting: Hyena hunting error bar
errorbar(HyenaHData,HyenaHModel,abs(HyenaHModel-HyenaHModelMin),abs(HyenaHModel-HyenaHModelMax),...
    abs(HyenaHData-HyenaHDataMin),abs(HyenaHData-HyenaHDataMax),'DisplayName','H, h',...
    'MarkerSize',15,'MarkerFaceColor',[0.490196079015732 0.180392161011696 0.552941203117371],...
    'Marker','o','LineStyle','none','LineWidth',1,'Color',[0.494117647409439 0.184313729405403 0.556862771511078]);
%plotting: Lion scavenging errorbar
errorbar(LionScavData,LionScavModel,abs(LionScavModel-LionScavModelMin),abs(LionScavModel-LionScavModelMax),...
    abs(LionScavData-LionScavDataMin),abs(LionScavData-LionScavDataMax),'DisplayName','Li, s',...
    'MarkerSize',15,'MarkerFaceColor',[0.466666668653488 0.674509823322296 0.18823529779911],'Marker','square',...
    'LineStyle','none','LineWidth',1,'Color',[0.466666668653488 0.674509823322296 0.18823529779911]);
%plotting: Hyena scavenging errorbar
errorbar(HyenaScavData,HyenaScavModel,abs(HyenaScavModel-HyenaScavModelMin),abs(HyenaScavModel-HyenaScavModelMax),...
    abs(HyenaScavData-HyenaScavDataMin),abs(HyenaScavData-HyenaScavDataMax),'DisplayName','H, s',...
    'MarkerSize',15,'MarkerFaceColor',[0.490196079015732 0.180392161011696 0.552941203117371],'Marker','square',...
    'LineStyle','none','LineWidth',1,'Color',[0.494117647409439 0.184313729405403 0.556862771511078]);

fprintf('Rsquare of best fit is %.4f\n',gof.rsquare)
fprintf('Best fit line is %.4fx + %.4f \n',fitobject.p1,fitobject.p2)

%labels
ylabel('Expected proportion of strategy, ');
xlabel('Observed proportion of strategy');
%axes stuff
xlim(axes1,[0 1.02]);
ylim(axes1,[0 1]);
box(axes1,'on');
hold(axes1,'off');
%Set the remaining axes properties
set(axes1,'FontSize',30);

% Create textbox
annotation(figure1,'textbox',...
    [0.725071097883596 0.181788051209104 0.195870535714286 0.10597439544808],...
    'String',{'Hunting','Scavenging'},...
    'FontSize',27,...
    'EdgeColor','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.604535383597881 0.269270270270272 0.123883928571429 0.0597439544807966],...
    'String',{'Hyena'},...
    'FontSize',27,...
    'EdgeColor','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.214285714285713 0.844950213371267 0.440848214285714 0.163584637268848],...
    'FontSize',27,...
    'FontName','Helvetica Neue',...
    'FitBoxToText','off',...
    'EdgeColor','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.616812169312166 0.42289758179232 0.0954241071428571 0.0597439544807966],...
    'String',{'Lion'},...
    'FontSize',27,...
    'EdgeColor','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.725071097883597 0.335415362731153 0.195870535714286 0.10597439544808],...
